/*
Brief Comparison of Data Types:

1. INT (Integer):
   - Numeric data type for storing whole numbers.
   - Uses fixed storage (e.g., typically 4 bytes).
   - Optimized for arithmetic operations and indexing.

2. VARCHAR(50) (Variable-Length String):
   - Stores up to 50 characters, using only the needed space plus a small overhead.
   - Efficient for text fields with varying lengths.
   - Helps save storage when lengths fluctuate.

3. CHAR(50) (Fixed-Length String):
   - Always occupies exactly 50 characters.
   - Shorter strings are padded with spaces to match the defined length.
   - Suitable for uniformly sized data where fixed length is required.
*/
